function showProductsCart(){
   $.ajax({
       url:'{{url("/getCartProducts")}}',
       method:'GET',
       success:function(data){
         $('.cart-body').html(data);
         totalCartProducts()
        }    
     });
 }
   totalCartProducts();
function totalCartProducts(){
   $.ajax({
       url:'{{url("/cartProductsCount")}}',
       method:'GET',
       success:function(data){
        if($.trim(data)!=0){
          $('.badge').show();
           $('.badge').html(data);
          }else{
             $('.badge').hide();
          }
        }    
     });
 }
 $(document).delegate('.delete_cart_product','click',function(){
    var cart_id=$(this).attr('id');
    deleteCartProduct(cart_id);
 })
 function deleteCartProduct(cart_id){
    $.ajax({
       url:'{{url("/deleteCartProduct")}}',
       data:{'cart_id':cart_id},
       method:'GET',
       success:function(data){
        if($.trim(data)=='true'){
            showProductsCart();
          }
        }    
     });
 }
